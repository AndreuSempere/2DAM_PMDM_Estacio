class VisitEntity {
  final String? id;
  final String nombre;
  final String email;
  final String motivo;
  final String dia;
  final String horainicio;
  final String horafin;

  VisitEntity({
    this.id,
    required this.nombre,
    required this.email,
    required this.motivo,
    required this.dia,
    required this.horainicio,
    required this.horafin,
  });
}
